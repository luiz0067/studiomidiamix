    <div class="textopadrao"> A Engeltec Equipamentos Ltda iniciou suas atividades em Toledo Paraná em outubro de 2002 e sua origem foi trabalhar com prestação de serviços (mão de obra). <br />
      <br />
      Em 2006 criamos a marca Maqsoft e nos estruturamos como Indústria em um mercado altamente competitivo que é o de máquinas de sorvete e máquinas de gelo.
      A Maqsoft pertence ao grupo Inomaq, onde esteve junto até dezembro de 2008. Em 2009, com a construção de sua sede própria, localizada na rua Dr. Olavo Secco Rigon, 139, 
      Parque Industrial Heriberto de Secco no Jardim Panorama. <br />
      <br />
      Hoje atendemos clientes de vários Estados do Brasil, com produtos de qualidade e confiabilidade, com uma veia sempre aberta para a inovação,
      buscando sempre a melhoria de seus produtos através de pesquisas constantes. O objetivo de Maqsoft é progredir sempre, acompanhando as novas tecnologias, 
      atendendo as necessidades dos seus clientes com soluções de qualidade. </div>
